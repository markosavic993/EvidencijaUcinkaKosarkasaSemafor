    private void inicijalizujTajmer() {
        timer = new javax.swing.Timer(200, new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                int min = Integer.parseInt(jlblMin.getText().trim());
                int sec = Integer.parseInt(jlblSec.getText().trim());
                if (min == 00 && sec == 00) {
                    timer.stop();
                    jbtnPokreni.setEnabled(false);
                    jbtnZaustavi.setEnabled(false);
                    jbtnPlus.setEnabled(false);
                    jbtnMinus.setEnabled(false);
                    iskljuciDugmice();
                    if (cetvrtina == 4) {
                        jbtnSacuvaj.setVisible(true);
                        return;
                    }
                    jtxtVreme.setEnabled(true);
                    jButton1.setEnabled(true);

                    return;
                }
                if (sec == 00) {
                    sec = 59;
                    min--;
                } else {
                    sec--;
                }
                if (min < 10) {
                    jlblMin.setText("0" + min);
                    jlblSec.setText("" + sec);
                    if (sec < 10) {
                        jlblSec.setText("0" + sec);

                    }

                } else {
                    jlblMin.setText("" + min);
                    jlblSec.setText("" + sec);
                }
            }
        });
    }

