    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int vreme = Integer.parseInt(jtxtVreme.getText().trim());
            if (vreme < 10) {
                jlblMin.setText("0" + vreme);
            } else {
                jlblMin.setText("" + vreme);
            }
            jButton1.setEnabled(false);
            jtxtVreme.setEnabled(false);
            jbtnPokreni.setEnabled(true);
            jbtnZaustavi.setEnabled(false);
            jbtnPlus.setEnabled(false);
            jbtnMinus.setEnabled(false);
            iskljuciDugmice();
            cetvrtina++;
            jlblCetvrtina.setText("" + cetvrtina);
            jtxtVreme.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Morate uneti celobrojnu vrednost!", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

