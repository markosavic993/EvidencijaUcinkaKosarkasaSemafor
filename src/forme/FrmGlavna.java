    private void jbtnPlusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPlusActionPerformed
        int min = Integer.parseInt(jlblMin.getText().trim());
        int sec = Integer.parseInt(jlblSec.getText().trim());

        if (sec == 59) {
            min++;
            sec = 0;
        } else {
            sec++;
        }

        if (min < 10) {
            jlblMin.setText("0" + min);
            jlblSec.setText("" + sec);
            if (sec < 10) {
                jlblSec.setText("0" + sec);

            }

        } else {
            jlblMin.setText("" + min);
            jlblSec.setText("" + sec);
        }
    }//GEN-LAST:event_jbtnPlusActionPerformed

    private void jbtnMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnMinusActionPerformed
        int min = Integer.parseInt(jlblMin.getText().trim());
        int sec = Integer.parseInt(jlblSec.getText().trim());

        if (sec == 00) {
            min--;
            sec = 59;
        } else {
            sec--;
        }

        if (min == 00 && sec == 00) {
            jbtnMinus.setEnabled(false);
            jbtnPlus.setEnabled(false);
            jbtnPokreni.setEnabled(false);
            jtxtVreme.setEnabled(true);
            jButton1.setEnabled(true);
            if (cetvrtina == 4) {
                jtxtVreme.setEnabled(false);
                jButton1.setEnabled(false);
                jbtnSacuvaj.setVisible(true);
            }
        }

        if (min < 10) {
            jlblMin.setText("0" + min);
            jlblSec.setText("" + sec);
            if (sec < 10) {
                jlblSec.setText("0" + sec);

            }

        } else {
            jlblMin.setText("" + min);
            jlblSec.setText("" + sec);
        }
    }//GEN-LAST:event_jbtnMinusActionPerformed

